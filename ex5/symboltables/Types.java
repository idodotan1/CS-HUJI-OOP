package ex5.symboltables;

/**
 * enum for the accepted types of the variables
 */
public enum Types {
        /**
         * For integers
         */
        intType,
        /**
         * For integers or non-integers
         */
        doubleType,
        /**
         * For boolean
         */
        booleanType,
        /**
         * For single characters
         */
        charType,
        /**
         * For strings
         */
        stringType
}


